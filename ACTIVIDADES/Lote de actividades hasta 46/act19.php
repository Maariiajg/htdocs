<?php
$a = 5;

echo "Valor inicial de a: $a<br>";

// Pre-incremento 
echo "Pre-incremento (++\$a): " . (++$a) . "<br>";
echo "Valor de a después del pre-incremento: $a<br>";

// Reiniciar $a
$a = 5;

// Post-incremento 
echo "Post-incremento (\$a++): " . ($a++) . "<br>";
echo "Valor de a después del post-incremento: $a<br>";
?>
